package home_work_1;

import java.lang.Math;

public class Task3 {
    public static void main(String[] args) {
        double a = 5.5;
        System.out.println(Math.round(a)); // Округление числа по математическим правилам
        // Вывод: 6

        int b = -5;
        System.out.println(Math.abs(b)); // Выводит модуль числа
        //Вывод: 5

        int c = 4;
        System.out.println(Math.sqrt(c)); // Выводит квадратный корень числа
        // Вывод: 2
    }
}

